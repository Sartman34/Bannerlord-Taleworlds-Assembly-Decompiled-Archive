using System.Collections.Generic;
using System.Xml;
using TaleWorlds.ObjectSystem;

namespace TaleWorlds.Core;

public class TradeItemComponent : ItemComponent
{
	public int MoraleBonus { get; private set; }

	internal static void AutoGeneratedStaticCollectObjectsTradeItemComponent(object o, List<object> collectedObjects)
	{
		((TradeItemComponent)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	public TradeItemComponent()
	{
		MoraleBonus = 0;
	}

	public TradeItemComponent(TradeItemComponent a)
	{
		MoraleBonus = a.MoraleBonus;
	}

	public override ItemComponent GetCopy()
	{
		return new TradeItemComponent(this);
	}

	public override void Deserialize(MBObjectManager objectManager, XmlNode node)
	{
		Initialize();
		MoraleBonus = XmlHelper.ReadInt(node, "morale_bonus");
	}
}
