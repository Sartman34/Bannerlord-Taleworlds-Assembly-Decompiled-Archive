using System.Collections.Generic;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.ObjectSystem;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.LogEntries;

public class ClanDestroyedLogEntry : LogEntry, IEncyclopediaLog, IChatNotification
{
	[SaveableField(10)]
	private Clan _clan;

	public bool IsVisibleNotification => true;

	internal static void AutoGeneratedStaticCollectObjectsClanDestroyedLogEntry(object o, List<object> collectedObjects)
	{
		((ClanDestroyedLogEntry)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		collectedObjects.Add(_clan);
	}

	internal static object AutoGeneratedGetMemberValue_clan(object o)
	{
		return ((ClanDestroyedLogEntry)o)._clan;
	}

	public ClanDestroyedLogEntry(Clan clan)
	{
		_clan = clan;
	}

	public bool IsVisibleInEncyclopediaPageOf<T>(T obj) where T : MBObjectBase
	{
		return obj == _clan;
	}

	public TextObject GetEncyclopediaText()
	{
		TextObject textObject = GameTexts.FindText("str_clan_destroyed");
		textObject.SetTextVariable("CLAN", _clan.EncyclopediaLinkWithName);
		return textObject;
	}

	public TextObject GetNotificationText()
	{
		return GetEncyclopediaText();
	}
}
