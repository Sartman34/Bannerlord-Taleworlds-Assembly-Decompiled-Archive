		{
			return ((RevenueVillage)o)._customProgress;
		}

		public void SetDone()
		{
			_isDone = true;
		}

		public RevenueVillage(Village village, int targetAmount)
		{
			Village = village;
			TargetAmount = targetAmount;
			CollectedAmount = 0;
			HourlyGain = targetAmount / 10;
			_isDone = false;
			EventOccurred = false;
