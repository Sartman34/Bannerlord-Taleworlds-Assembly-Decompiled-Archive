using System.Collections.Generic;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.MapNotificationTypes;

public class ArmyDispersionMapNotification : InformationData
{
	public override TextObject TitleText => new TextObject("{=84vpd3LI}Army Dispersed");

	public override string SoundEventPath => "event:/ui/notification/army_dispersion";

	[SaveableProperty(1)]
	public Army DispersedArmy { get; private set; }

	[SaveableProperty(2)]
	public Army.ArmyDispersionReason DispersionReason { get; private set; }

	internal static void AutoGeneratedStaticCollectObjectsArmyDispersionMapNotification(object o, List<object> collectedObjects)
	{
		((ArmyDispersionMapNotification)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		collectedObjects.Add(DispersedArmy);
	}

	internal static object AutoGeneratedGetMemberValueDispersedArmy(object o)
	{
		return ((ArmyDispersionMapNotification)o).DispersedArmy;
	}

	internal static object AutoGeneratedGetMemberValueDispersionReason(object o)
	{
		return ((ArmyDispersionMapNotification)o).DispersionReason;
	}

	public ArmyDispersionMapNotification(Army dispersedArmy, Army.ArmyDispersionReason reason, TextObject descriptionText)
		: base(descriptionText)
	{
		DispersionReason = reason;
		DispersedArmy = dispersedArmy;
	}
}
