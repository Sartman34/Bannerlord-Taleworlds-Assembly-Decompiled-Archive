using System.Collections.Generic;
using TaleWorlds.Core;
using TaleWorlds.Localization;
using TaleWorlds.SaveSystem;

namespace TaleWorlds.CampaignSystem.MapNotificationTypes;

public class ArmyCreationMapNotification : InformationData
{
	public override TextObject TitleText => new TextObject("{=6NWExCu6}Army Created");

	public override string SoundEventPath => "event:/ui/notification/army_created";

	[SaveableProperty(1)]
	public Army CreatedArmy { get; private set; }

	internal static void AutoGeneratedStaticCollectObjectsArmyCreationMapNotification(object o, List<object> collectedObjects)
	{
		((ArmyCreationMapNotification)o).AutoGeneratedInstanceCollectObjects(collectedObjects);
	}

	protected override void AutoGeneratedInstanceCollectObjects(List<object> collectedObjects)
	{
		base.AutoGeneratedInstanceCollectObjects(collectedObjects);
		collectedObjects.Add(CreatedArmy);
	}

	internal static object AutoGeneratedGetMemberValueCreatedArmy(object o)
	{
		return ((ArmyCreationMapNotification)o).CreatedArmy;
	}

	public ArmyCreationMapNotification(Army createdArmy, TextObject description)
		: base(description)
	{
		CreatedArmy = createdArmy;
	}
}
